cars = ["bmw", "audi", "toyota", "subaru"]
#字典顺序sort
cars.sort()
print(cars)
#字典逆序sort reverse
cars.sort(reverse=True)
print(cars)
#临时排序sorted
print(sorted(cars))
print(cars)
cars.sort()
print(sorted(cars,reverse=True))
print(cars)

#逆序
cars = ["bmw", "audi", "toyota", "subaru"]
cars.reverse()
print(cars)

#长度len
print(len(cars))